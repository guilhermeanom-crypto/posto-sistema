import {
  Activity,
  Building2,
  CalendarClock,
  ClipboardList,
  Compass,
  Droplets,
  FileText,
  FlaskConical,
  FolderSearch,
  Fuel,
  HardHat,
  Landmark,
  Leaf,
  type LucideIcon,
  Map,
  MapPin,
  Mountain,
  Scale,
  Search,
  ShieldCheck,
  Stamp,
  TreePine,
  Truck,
  Users,
  Wind,
} from 'lucide-react'

const ICONS: Record<string, LucideIcon> = {
  Activity,
  Building2,
  CalendarClock,
  ClipboardList,
  Compass,
  Droplets,
  FileText,
  FlaskConical,
  FolderSearch,
  Fuel,
  HardHat,
  Landmark,
  Leaf,
  Map,
  MapPin,
  Mountain,
  Scale,
  Search,
  ShieldCheck,
  Stamp,
  TreePine,
  Truck,
  Users,
  Wind,
}

export function iconFor(name?: string): LucideIcon {
  if (!name) return ShieldCheck
  return ICONS[name] ?? ShieldCheck
}
